def same_image?(path1, path2)
  return true if path1 == path2
  
  begin
    open(path1) {|image1| 
      open(path2) {|image2|
        return false if image1.size != image2.size
        while (b1 = image1.read(1024)) and (b2 = image2.read(1024))
          return false if b1 != b2
        end
      }
    }
    true
  rescue
    true
  end
end

